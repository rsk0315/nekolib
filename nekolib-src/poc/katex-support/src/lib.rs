//! $`\KaTeX`$ support
//!
//! Renders ``$`119\cdot 2^{23}+1`$`` into
//! $`119\cdot 2^{23}+1`$, while `$5\cdot 2^{25}+1$` into
//! $5\cdot 2^{25}+1$.
//! Also
//! ````text
//! ```math
//! f(x) = \int_{-\infty}^\infty
//! \hat f(\xi)\,e^{2 \pi i \xi x}\, \mathrm{d}\xi
//! ```
//! ````
//! into:
//! ```math
//! f(x) = \int_{-\infty}^\infty
//! \hat f(\xi)\,e^{2 \pi i \xi x}\, \mathrm{d}\xi
//! ```
